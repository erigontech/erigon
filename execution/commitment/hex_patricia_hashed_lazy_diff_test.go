// Test_HexPatriciaHashed_LazyVsEager_Differential runs the same deterministic
// dataset through two HexPatriciaHashed instances — one with lazy derivation
// (default, forceEagerDerive=false) and one with eager derivation
// (forceEagerDerive=true, pre-PR-19899 behaviour). It asserts byte-for-byte
// equality of root hashes AND persisted branch data in MockState.cm.
func Test_HexPatriciaHashed_LazyVsEager_Differential(t *testing.T) {
	t.Parallel()

	type testCase struct {
		name        string
		seed        int64
		nKeys       int
		withStorage bool
		rounds      int
		prefix      []byte // non-nil → use generateKeyWithHashedPrefix
	}

	cases := []testCase{
		{name: "small_acc", seed: 1, nKeys: 50, rounds: 2},
		{name: "medium_acc", seed: 2, nKeys: 500, rounds: 2},
		{name: "acc_plus_storage", seed: 3, nKeys: 200, withStorage: true, rounds: 2},
		{name: "multi_round_acc", seed: 4, nKeys: 100, rounds: 5},
		{name: "large_acc", seed: 5, nKeys: 2000, rounds: 2},
		{name: "deep_shared_prefix", seed: 6, nKeys: 50, rounds: 3, prefix: []byte{0x0a, 0x0b, 0x0c}},
	}

	var anyDeferred atomic.Uint64

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			deferred := runDifferentialFromSeed(t, tc.seed, tc.nKeys, tc.withStorage, tc.rounds, tc.prefix)
			anyDeferred.Add(deferred)
			t.Logf("cellsDeferred=%d", deferred)
		})
	}

	// Meta-assertion: at least one lazy case must have observed cellsDeferred > 0,
	// proving the lazy code path was actually exercised.
	t.Cleanup(func() {
		require.Greater(t, anyDeferred.Load(), uint64(0),
			"no test case observed cellsDeferred > 0 — the lazy code path was never exercised")
	})
}

// runDifferentialFromSeed builds a deterministic dataset from seed and runs it
// through both a lazy trie and an eager trie, asserting equivalence of root
// hashes and persisted branch bytes after every round.
// Returns the cumulative cellsDeferred count from the lazy trie.
func runDifferentialFromSeed(t *testing.T, seed int64, nKeys int, withStorage bool, rounds int, prefix []byte) uint64 {
	t.Helper()
	ctx := context.Background()

	msL := NewMockState(t)
	msE := NewMockState(t)
	trieL := NewHexPatriciaHashed(length.Addr, msL)
	trieE := NewHexPatriciaHashed(length.Addr, msE)
	trieE.SetForceEagerDerive(true)

	updsL := newEmptyUpdates(t)
	defer updsL.Close()
	updsE := newEmptyUpdates(t)
	defer updsE.Close()

	rnd := rand.New(rand.NewSource(seed))

	for round := 0; round < rounds; round++ {
		plainKeys, updates := buildRound(t, rnd, nKeys, withStorage, prefix)

		require.NoError(t, msL.applyPlainUpdates(plainKeys, updates))
		require.NoError(t, msE.applyPlainUpdates(plainKeys, updates))

		WrapKeyUpdatesInto(t, updsL, plainKeys, updates)
		WrapKeyUpdatesInto(t, updsE, plainKeys, updates)

		rootL, err := trieL.Process(ctx, updsL, "", nil, WarmupConfig{})
		require.NoError(t, err)

		rootE, err := trieE.Process(ctx, updsE, "", nil, WarmupConfig{})
		require.NoError(t, err)

		require.Equal(t, rootE, rootL,
			"root hash differs (eager vs lazy) seed=%d round=%d", seed, round)

		assertBranchEquality(t, msE.cm, msL.cm, seed, round)
	}

	// Verify the eager trie never deferred any cells — if forceEagerDerive is
	// broken, both tries would be lazy and the comparison would be meaningless.
	require.Zero(t, trieE.cellsDeferred,
		"eager trie should have zero deferred cells (seed=%d)", seed)

	return trieL.cellsDeferred
}

// newEmptyUpdates creates a fresh *Updates object with no keys, suitable for
// subsequent WrapKeyUpdatesInto calls.
func newEmptyUpdates(t *testing.T) *Updates {
	t.Helper()
	return NewUpdates(ModeDirect, t.TempDir(), KeyToHexNibbleHash)
}

// buildRound constructs one round of deterministic updates from the given rng.
// If prefix is non-nil, keys are generated by brute-forcing with rnd until the
// keccak hash starts with the given nibble prefix — fully deterministic from seed.
// If withStorage is true, each account also gets one random storage slot.
func buildRound(t *testing.T, rnd *rand.Rand, nKeys int, withStorage bool, prefix []byte) ([][]byte, []Update) {
	t.Helper()
	builder := NewUpdateBuilder()

	for i := 0; i < nKeys; i++ {
		var addrHex string
		if prefix != nil {
			pk := generateKeyWithHashedPrefixSeeded(rnd, prefix, length.Addr)
			addrHex = fmt.Sprintf("%x", pk)
		} else {
			key := make([]byte, length.Addr)
			rnd.Read(key)
			addrHex = fmt.Sprintf("%x", key)
		}
		builder.Balance(addrHex, rnd.Uint64())

		if withStorage {
			slot := make([]byte, length.Hash)
			rnd.Read(slot)
			val := make([]byte, 1+rnd.Intn(31))
			rnd.Read(val)
			builder.Storage(addrHex, fmt.Sprintf("%x", slot), fmt.Sprintf("%x", val))
		}
	}

	return builder.Build()
}

// generateKeyWithHashedPrefixSeeded brute-forces a plain key whose keccak hash
// starts with the given nibble prefix, using the provided RNG for full
// reproducibility from a seed.
func generateKeyWithHashedPrefixSeeded(rnd *rand.Rand, constHashedPrefixNibbles []byte, keyLen int) []byte {
	plainKey := make([]byte, keyLen)
	for {
		rnd.Read(plainKey)
		hashedKey := KeyToNibblizedHash(plainKey)
		if bytes.HasPrefix(hashedKey, constHashedPrefixNibbles) {
			return plainKey
		}
	}
}

// assertBranchEquality compares two MockState.cm maps byte-for-byte.
func assertBranchEquality(t *testing.T, eager, lazy map[string]BranchData, seed int64, round int) {
	t.Helper()

	require.Equalf(t, len(eager), len(lazy),
		"branch count differs: eager=%d lazy=%d seed=%d round=%d",
		len(eager), len(lazy), seed, round)

	for prefix, eagerData := range eager {
		lazyData, ok := lazy[prefix]
		require.Truef(t, ok,
			"branch present in eager but missing in lazy at prefix %x (seed=%d round=%d)",
			prefix, seed, round)
		require.Equalf(t, eagerData, lazyData,
			"branch bytes differ at prefix %x (seed=%d round=%d)\n  eager: %x\n  lazy:  %x",
			prefix, seed, round, []byte(eagerData), []byte(lazyData))
	}

}

// FuzzHexPatriciaHashed_LazyVsEager exercises the lazy-vs-eager differential
// comparison with randomised inputs derived from a fuzz seed.
func FuzzHexPatriciaHashed_LazyVsEager(f *testing.F) {
	f.Add([]byte("erigon-lazy-derive-fuzz-seed"))
	f.Add(make([]byte, 64))
	f.Add([]byte("short"))

	f.Fuzz(func(t *testing.T, seed []byte) {
		if len(seed) == 0 {
			t.Skip("empty seed")
		}
		s := int64(crc32.ChecksumIEEE(seed))
		runDifferentialFromSeed(t, s, 64, false, 3, nil)
	})
}
