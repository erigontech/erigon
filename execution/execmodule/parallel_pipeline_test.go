// TestConcurrentPatriciaHashed_ManyBlocks exercises the ConcurrentPatriciaHashed
// trie through the full exec3 pipeline with multiple blocks and many accounts.
// This verifies that the concurrent wrapper produces correct state roots when
// used through the real staged sync execution path.
//
// If this test fails with "wrong trie root", it means the ConcurrentPatriciaHashed
// (which may enable ParallelHashSort after CanDoConcurrentNext returns true)
// computes different roots than the block headers generated by GenerateChain
// (which uses the serial path).
func TestConcurrentPatriciaHashed_ManyBlocks(t *testing.T) {
	if testing.Short() {
		t.Skip()
	}
	ctx := t.Context()

	// Create multiple funded accounts for diverse transactions
	numAccounts := 20
	keys := make([]*ecdsa.PrivateKey, numAccounts)
	addrs := make([]common.Address, numAccounts)
	alloc := make(types.GenesisAlloc)
	for i := 0; i < numAccounts; i++ {
		key, err := crypto.GenerateKey()
		require.NoError(t, err)
		keys[i] = key
		addrs[i] = crypto.PubkeyToAddress(key.PublicKey)
		alloc[addrs[i]] = types.GenesisAccount{
			Balance: new(big.Int).Exp(big.NewInt(10), big.NewInt(20), nil), // 100 ETH
		}
	}

	genesis := &types.Genesis{
		Config: chain.AllProtocolChanges,
		Alloc:  alloc,
	}

	m := execmoduletester.New(
		t,
		execmoduletester.WithGenesisSpec(genesis),
		execmoduletester.WithKey(keys[0]),
	)

	// Generate 20 blocks with cross-account transfers
	numBlocks := 20
	chainPack, err := blockgen.GenerateChain(m.ChainConfig, m.Genesis, m.Engine, m.DB, numBlocks, func(i int, b *blockgen.BlockGen) {
		// Each block: 3-5 transfers between random accounts
		numTxns := 3 + (i % 3)
		for j := 0; j < numTxns; j++ {
			from := (i*numTxns + j) % numAccounts
			to := (from + 1 + j) % numAccounts
			nonce := b.TxNonce(addrs[from])
			tx, err := types.SignTx(
				types.NewTransaction(nonce, addrs[to], uint256.NewInt(1000), 21000, uint256.NewInt(m.Genesis.BaseFee().Uint64()+1), nil),
				*types.LatestSignerForChainID(m.ChainConfig.ChainID),
				keys[from],
			)
			require.NoError(t, err)
			b.AddTx(tx)
		}
	})
	require.NoError(t, err)
	require.Len(t, chainPack.Blocks, numBlocks)

	// Insert all blocks and validate each one
	err = insertValidateAndUfc1By1(ctx, m.ExecModule, chainPack.Blocks)
	require.NoError(t, err)

	t.Logf("successfully processed %d blocks through ConcurrentPatriciaHashed pipeline", numBlocks)
}
