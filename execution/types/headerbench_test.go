// loadHeadersRLP reads a sequence of length-prefixed RLP-encoded headers
// from a testdata file generated by extract_headers (see PR description).
// Format: repeated [u32 LE length][rlp bytes].
func loadHeadersRLP(tb testing.TB, name string) [][]byte {
	tb.Helper()
	path := filepath.Join("testdata", name)
	raw, err := os.ReadFile(path)
	if err != nil {
		tb.Fatalf("read %s: %v", path, err)
	}
	var out [][]byte
	for len(raw) >= 4 {
		n := binary.LittleEndian.Uint32(raw[:4])
		raw = raw[4:]
		if uint32(len(raw)) < n {
			tb.Fatalf("truncated record in %s", path)
		}
		out = append(out, raw[:n])
		raw = raw[n:]
	}
	if len(out) == 0 {
		tb.Fatalf("no headers in %s", path)
	}
	return out
}

// BenchmarkDecodeHeader_PreLondon decodes a single pre-London header (no BaseFee).
// Mainnet epoch 99, ~Sep 2015, Frontier-era headers.
func BenchmarkDecodeHeader_PreLondon(b *testing.B) {
	headers := loadHeadersRLP(b, "headers-mainnet-00099.rlp")
	hRLP := headers[0]
	b.ResetTimer()
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		var h Header
		if err := rlp.DecodeBytes(hRLP, &h); err != nil {
			b.Fatal(err)
		}
	}
}

