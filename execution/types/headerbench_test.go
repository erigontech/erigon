// BenchmarkDecodeHeader_Loop_PreLondon walks 100 pre-London headers per op,
// mirroring the freezeblocks.ForEachHeader pattern: var header in the loop,
// rlp.DecodeBytes(payload, &header), hand to a no-op consumer.
func BenchmarkDecodeHeader_Loop_PreLondon(b *testing.B) {
	headers := loadHeadersRLP(b, "headers-mainnet-00099.rlp")
	b.ResetTimer()
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		for _, hRLP := range headers {
			var h Header
			if err := rlp.DecodeBytes(hRLP, &h); err != nil {
				b.Fatal(err)
			}
			_ = h.ParentHash // prevent dead-code elimination
		}
	}
}

