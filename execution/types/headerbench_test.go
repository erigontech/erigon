// BenchmarkEncodeHeader_London round-trips one header through EncodeToBytes.
func BenchmarkEncodeHeader_London(b *testing.B) {
	headers := loadHeadersRLP(b, "headers-mainnet-01894.rlp")
	var h Header
	if err := rlp.DecodeBytes(headers[0], &h); err != nil {
		b.Fatal(err)
	}
	b.ResetTimer()
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		out, err := rlp.EncodeToBytes(&h)
		if err != nil {
			b.Fatal(err)
		}
		_ = out
	}
}

// BenchmarkHeaderHash_Mutable measures Hash() on a fresh (mutable) header —
// hash is recomputed each call. This is the walker-loop pattern.
func BenchmarkHeaderHash_Mutable(b *testing.B) {
	headers := loadHeadersRLP(b, "headers-mainnet-01894.rlp")
	var h Header
	if err := rlp.DecodeBytes(headers[0], &h); err != nil {
		b.Fatal(err)
	}
	h.mutable = true
	b.ResetTimer()
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		_ = h.Hash()
	}
}

// BenchmarkHeaderHash_Memoized measures Hash() with the atomic memoization
// path active (h.mutable == false, first call computes, subsequent calls
// return the cached value).
func BenchmarkHeaderHash_Memoized(b *testing.B) {
	headers := loadHeadersRLP(b, "headers-mainnet-01894.rlp")
	var h Header
	if err := rlp.DecodeBytes(headers[0], &h); err != nil {
		b.Fatal(err)
	}
	h.mutable = false
	_ = h.Hash() // warm the cache
	b.ResetTimer()
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		_ = h.Hash()
	}
}
