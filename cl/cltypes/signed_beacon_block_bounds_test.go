// TestSignedBeaconBlock_DecodeSSZ_MalformedTransactions reproduces security#66:
// a crafted 1124-byte Fulu SignedBeaconBlock SSZ where the Transactions field
// is [0x08,0x00,0x00,0x00] — implying 2 transactions in a 4-byte buffer.
// Before the fix, this caused a panic in TransactionsSSZ.DecodeSSZ
// (index out of range [3] with length 0). Now it must return an error.
func TestSignedBeaconBlock_DecodeSSZ_MalformedTransactions(t *testing.T) {
	version := clparams.FuluVersion
	beaconCfg := clparams.MainnetBeaconConfig

	block := NewSignedBeaconBlock(&beaconCfg, version)
	err := block.DecodeSSZ(maliciousSignedBeaconBlockSSZ, int(version))
	if err == nil {
		t.Fatal("Expected error decoding malicious block with crafted Transactions field, got nil")
	}
	t.Logf("Got expected error: %v", err)
}

